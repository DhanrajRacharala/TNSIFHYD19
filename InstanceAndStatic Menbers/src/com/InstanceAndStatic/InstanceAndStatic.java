package com.InstanceAndStatic;

public class InstanceAndStatic {

	public int a = 35;
	
	public void display() {
		System.out.println("Hi good to see you");
	}
	
	public int getInteger(int a) {
		return a;
	}
	
	static int b = 35;
	
	static void display1() {
		System.out.println("Hi nice to meet you");
	}
	
	static int getinteger1(int a) {
		return a;
	}
	
	

}
